public class Ex6 {
    public static void main(String[] args) {
        int c1 = 69;
        int c2 = 53;
        int c3 = 63;

        int resultadoA = c1 + 1;
        int resultadoB = c1 - c2 + c3;
        int resultadoC = c1 - 2;
        int resultadoD = c2 - 50;
        int resultadoE = c3 + 36 ;
        int resultadoF = 50 + 50;
        int resultadoG = 3 * c2;
        int resultadoH = 51 * c2;


        System.out.println("a) c1 + 1 = " +  resultadoA);
        System.out.println("b) c1 - c2 + c3 = " + resultadoB);
        System.out.println("c) c1 - 2 = " + resultadoC);
        System.out.println("d) c2 - '2' = " + resultadoD);
        System.out.println("e) c3 + '#' = " + resultadoE);
        System.out.println("f) '2' + '2' = " + resultadoF);
        System.out.println("g) 3 * c2 = " + resultadoG);
        System.out.println("h) '3' * c2 = " + resultadoH);
    }
}
